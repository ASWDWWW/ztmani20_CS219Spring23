package CS250;

public class Util {
    /***
     *
     * @param a first integer
     * @param b second integer
     * @param c thrid integer
     * @return the average of the 3 numbers
     */
    public static int averageOf3(int a, int b, int c) {
        int add = a + b + c;
        int divide = add / 3;
        return divide;

    }
}
